import UIKit
import UIModule

// Custom Colors
extension UIColor {
    public class var errorBgColor:UIColor {
        return UIColor(hex: "c0392b")
    }
    
    public class var warningBgColor:UIColor {
        return UIColor(hex: "f1c40f")
    }
}
